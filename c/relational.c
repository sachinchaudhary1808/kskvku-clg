#include <stdio.h>

void main() {
	int a,b;
	printf("Plz enter the value for int a: ");
	scanf("%d",&a);
	printf("Plz enter the value for int b: ");
	scanf("%d",&b);
	printf("%d > %d -> %d\n",a,b,(a>b));
	printf("%d < %d -> %d\n",a,b,(a<b));
	printf("%d >= %d -> %d\n",a,b,(a>=b));
	printf("%d <= %d -> %d\n",a,b,(a<=b));
}
